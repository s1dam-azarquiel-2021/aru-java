package ejercicio5;

public class Demarcacion {
	private String idDemarcacion;
	private String nombre;

	public Demarcacion(String idDemarcacion, String nombre) {
		super();
		this.idDemarcacion = idDemarcacion;
		this.nombre = nombre;
	}

	public String getIdDemarcacion() {
		return this.idDemarcacion;
	}

	public String getNombre() {
		return this.nombre;
	}
}
